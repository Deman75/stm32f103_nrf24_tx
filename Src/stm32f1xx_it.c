/**
* @brief This function handles TIM3 global interrupt.
*/
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
	
	if ((But1.ButFlag == 0)&&(Button1 == GPIO_PIN_RESET)){
		But1.ButFlag = 1;
		But1.ButDown = 1;
		But1.ButEvent = 0;
		But1.ButEventTime = 0;
	}
	if (But1.ButFlag == 1) {
		But1.ButDownTime++;
	}
	if (((But1.ButFlag == 1) && (Button1 == GPIO_PIN_SET)
	&& (But1.ButDownTime>10))||(But1.ButDownTime>2000)){
			
		if (But1.ButDownTime<50){
					But1.ButEvent = 1;
				}
			else if (But1.ButDownTime<500){
					But1.ButEvent = 2;
				}
			else
				But1.ButEvent = 0;
		
		But1.ButFlag = 0;
		But1.ButDown = 0;
		But1.ButDownTime = 0;
	}
	
	if (But1.ButEvent != 0)
		But1.ButEventTime++;
	if (But1.ButEventTime > 200){
		But1.ButEventTime = 0;
		But1.ButEvent = 0;
	}
	if (But1.ButEvent == 0 && But1.ButEventTime != 0)
		But1.ButDownTime = 0;

  /* USER CODE END TIM3_IRQn 1 */
}

