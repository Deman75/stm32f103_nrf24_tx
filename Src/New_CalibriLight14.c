uint8_t CharNweCalibriLight14 (uint16_t x, uint16_t y, unsigned char ch, uint8_t CharColor, uint8_t BkgColor)
{
	uint8_t i, j, w, cha, posX=0;
	uint8_t Width = (CalibriLight14_Height * CalibriLight14_Width)+1;
	uint8_t SymbolWidth=0;
	for (i=1;i<=Width-1;i++)
		{
			for (w=0;w<=CalibriLight14_Width-1;w++)
				{
					if (ch<=0x7F)
						{
							cha=CalibriLight14[((ch-0x20)*(Width)) + i];
							SymbolWidth=CalibriLight14[((ch-0x20)*(Width))];
						}
					if (ch>=0xA0)
						{
							cha=CalibriLight14[((ch-0x20-0x20)*(Width)) + i];
							SymbolWidth=CalibriLight14[((ch-0x20-0x20)*(Width))];
						}
					if (ch>0x80 && ch<0xA0)
						{
							cha=CalibriLight14[(ch-0x20)+i];
							SymbolWidth=CalibriLight14[((ch-0x20)*(Width))];
						}
					for (j=0;j<=7;j++)
						{
							if (cha & 0x80)
									LCD_DrawPixel(x+posX,y+j+(w*7)+w,CharColor);
								else
									if (((j+(w*7)+w)<=SymbolWidth)&&(BkgColor != 0x02))
										LCD_DrawPixel(x+posX,y+j+(w*7)+w,BkgColor);
							cha=cha<<1;
						}
					if (w<CalibriLight14_Width-1)
						i++;
				}
			posX++;	
		}
	return SymbolWidth;
}



