void StringCalibriLight14 (uint16_t x, uint16_t y, char *string, uint8_t CharColor, uint8_t BkgColor)
{
	uint8_t SymbolWidth;
	while (*string != 0)
		{
			SymbolWidth=CharNweCalibriLight14(x,y,*string,CharColor,BkgColor);
			y=y+SymbolWidth;
			string++;
		}
}


